// 标志位
#define THINGCLASS_FLAG_CREATURE		0x00000100	// 为生物
#define THINGCLASS_FLAG_EQUIPMENT		0x00000200	// 为装备
#define THINGCLASS_FLAG_MEDICAMENT		0x00000400	// 为药品
#define THINGCLASS_FLAG_WARRANT			0x00000800	// 为凭证
#define THINGCLASS_FLAG_VALUABLES		0x00001000	// 为元宝
//------------------------------------------------------------------------
// 定义
struct IThingClass
{
	// 返回类型值
	virtual DWORD	GetClassVal(void) = NULL;
	// 返回诸如THINGCLASS_ACTOR, THINGCLASS_MONSTER这种, 也就是低八位啦
	virtual DWORD	GetThingClass(void) = NULL;
	// 返回是否为生物
	virtual BOOL	IsCreature(void) = NULL;
	// 返回是否为装备
	virtual BOOL	IsEquipment(void) = NULL;
	// 返回是否为药品
	virtual BOOL	IsMedicament(void) = NULL;
	// 返回是否为凭证
	virtual BOOL	IsWarrant(void) = NULL;
	// 返回是否为元宝
	virtual BOOL	IsValuables(void) = NULL;
};
