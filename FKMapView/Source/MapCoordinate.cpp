/*
 * 功能：实现45度地图坐标计算，如由象素坐标获取图块坐标
 *		 逻辑上，地图块系统是一个正方形的数组，但显示窗口为一个菱形
 * 约定：
		1、 名词：
				象素坐标系(或屏幕坐标系)：
						(x,y)标准坐标系，用于鼠标界面操作,(0,0)点在左上角
						一般用(x,y)表示
				块坐标系：  
						45度斜视角的菱形块坐标，(0,0)点在最左下角
						一般用(tx,ty)表示
				块区域：
						定义为(left,top,row,col),其中(row,col)对应于(kx,ky)
						块区域用于表示屏幕上的一个象素矩形对应的块坐标
						块区域对应于地图上的矩形块
						一般用rcTileArea表示
				块矩阵:
						定义为(left,top,right,bottom)
						块矩阵对应于地图上的菱形块
  						一般用rcTileRect表示
				K坐标:
						辅助计算坐标，象素坐标中以64x32为单位的块
		  ^
		  |ty
		  |
		  *---U--+-----
		  |	 /\	 |
		  |	/  \ |
		  |/	\|
		 L|\	/|R
		  |	\  / |
		  |  \/	 |
		  O---D--+--->tx

		  |<---->|	对块坐标系来说，这个宽度 = row + col
		  LU = DR = col，块坐标系宽度
		  LD = UR = row，块坐标系高度

		 O位置上，(tx,ty) = (0,0)，O点是块坐标系原点
		 L位置上，(tx,ty) = (0,row),row为整个地图块矩阵的高
		 *位置上，(x,y) = (0,0)，*点为象素坐标系原点

		菱形区域内是45度有效块范围，矩形内其余部分的块是无效块，但坐标是有效的

		2、 (x,y)表示象素坐标，是用于屏幕操作计算的
			(tx,ty)一般表示菱形块坐标，即(tilex,tiley)，是基于斜45度坐标的
			(mx,my)表示游戏世界坐标，块坐标要映射到一个正方形的矩阵中，(MaxtrixX,MatrixY)

  		    (kx,ky)，辅助计算坐标，把游戏世界分割成以64x32正方形单元，每个单元的横纵坐标为(kx,ky)，是基于(x,y)坐标系的

		3、相关图形请参考vss上的相关jpg文件： $\dxGame\Documents\游戏引擎
 * 算法：	
		1、每行采用dwWidth + dwX0 + tileX0 + tileX1.....的形式存储每一行		   
		2、函数关系如下:
			tx = kx + ky;
			ty = kx - ky;
			根据这个公式也可以求出(kx,ky)
			kx = (tx + ty) / 2
			ky = (tx - ty) / 2

 */
