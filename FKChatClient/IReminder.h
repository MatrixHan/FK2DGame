// 提示器类型
enum EReminderType
{
	REMINDER_ROLL,			// 滚动显示
	REMINDER_REPLACE,		// 顶替显示
	REMINDER_ADD			// 添加显示
};
//------------------------------------------------------------------------
// 系统信息提示器
struct IReminder
{
	//-------------------------------------------------------------------
	// 描  述：画图 
	//-------------------------------------------------------------------
	virtual void OnDraw(ICanvas * pCanvas) = 0;

	//-------------------------------------------------------------------
	// 描  述：设置提示器的类型
	// 参  数：[输入] int nType - 类型	
	// 返回值：void
	//-------------------------------------------------------------------
	virtual void SetType(int nType) = 0;

	//-------------------------------------------------------------------
	// 描  述：设置提示器的位置
	// 参  数：[输入] RECT & rc - 位置	
	// 返回值：void
	//-------------------------------------------------------------------
	virtual void SetPos(RECT & rc) = 0;

	//-------------------------------------------------------------------
	// 描  述：添加提示信息
	// 参  数：[输入] LPCSTR lpszText - 信息文本
	// 返回值：void
	//-------------------------------------------------------------------
	virtual void AddMessage(LPCSTR lpszText) = 0;
	
	//-------------------------------------------------------------------
	// 描  述：清空提示信息
	//-------------------------------------------------------------------
	virtual void ClearMessage(void) = 0;
};
